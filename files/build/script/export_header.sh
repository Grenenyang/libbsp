#!/bin/bash
set -e  # 出错时立即退出

# ===================== 配置项（根据你的需求修改）=====================
# 1. 要导出的源.h文件列表（按你需要的顺序排列）
SOURCE_HEADERS=(
    "${PRJ_ROOT}/usource/core/export.h"
    "${PRJ_ROOT}/usource/switch/export.h"
    # 可添加更多.h文件，比如 "${PRJ_ROOT}/include/wt_tools_core.h"
)

# 2. 对外导出的目标文件路径
EXPORT_HEADER="${PRJ_BUILD_ROOT}/include/libbsp.h"

# 3. 导出文件的保护宏（防止重复包含）
GUARD_MACRO="__LIBBSP_H"

# ===================== 脚本核心逻辑 =====================

# 清空原有导出文件（或创建新文件）
> ${EXPORT_HEADER}

# 写入保护宏开头
echo "/* Auto-generated by export_header.sh - DO NOT EDIT MANUALLY */" >> ${EXPORT_HEADER}
echo "/* Generate time: $(date +%Y-%m-%d\ %H:%M:%S) */" >> ${EXPORT_HEADER}
echo "#ifndef ${GUARD_MACRO}" >> ${EXPORT_HEADER}
echo "#define ${GUARD_MACRO}" >> ${EXPORT_HEADER}
echo "" >> ${EXPORT_HEADER}

# 逐个读取源.h文件，整合内容（过滤源文件的保护宏）
for header in "${SOURCE_HEADERS[@]}"; do
    echo ${header}
    if [ -f "${header}" ]; then
	# 写入源文件标注（方便区分不同文件的内容）
        echo "/* ========================================================== */" >> ${EXPORT_HEADER}
        echo "/* Source file: ${header} */" >> ${EXPORT_HEADER}
        echo "/* ========================================================== */" >> ${EXPORT_HEADER}
        echo "" >> ${EXPORT_HEADER}

        # 直接读取源文件所有内容（保留保护宏、注释、代码）
        cat ${header} >> ${EXPORT_HEADER}

        echo "" >> ${EXPORT_HEADER}  # 空行分隔不同文件，提升可读性
        echo "✅ 整合文件（保留保护宏）：${header}"
    else
        echo "⚠️  警告：源文件不存在，跳过 -> ${header}"
    fi
done

# 写入保护宏结尾
echo "#endif /* ${GUARD_MACRO} */" >> ${EXPORT_HEADER}

echo ""
echo "🎉 对外导出文件生成完成："
echo "   路径：${EXPORT_HEADER}"
echo "   包含源文件数量：${#SOURCE_HEADERS[@]}"
